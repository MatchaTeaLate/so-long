#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst != NULL)
	{	
		if (lst->content)
		{
			f(lst->content);
			lst = lst->next;
		}
	}
}
